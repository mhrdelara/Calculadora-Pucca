function adicionarValor(valor){
    let input = document.getElementById("text")
    input.value += valor
}

function apagarValor(){
    let input = document.getElementById("text")
    input.value = ""
}

function aparecerValor(){
    let input = document.getElementById("text")
    input.value = eval(input.value)
}

